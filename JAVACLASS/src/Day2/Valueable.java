package Day2;

public class Valueable {
    // Keyword : 선언 - 초기화

    public static void main(String[] args) {
        int first_val; //메모리 공간에다가 정수가 들어갈 'first_val'이라는 이름의 상자를 놓는다. (선언)
        first_val =1; //상자 안에 1이라는 숫자를 넣는다. (초기화)
        System.out.println(first_val);

        int second_val = 2; //선언과 초기화는 함께 이루어질 수 있다.
        System.out.println(second_val);
    }

}
